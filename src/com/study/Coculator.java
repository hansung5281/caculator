package com.study;

public class Coculator {
	int Add(int num1, int num2)
	{
		int result = num1 + num2;
		return result;
	}
	int Minus(int num1, int num2)
	{
		int result = num1 - num2;
		return result;
	}
	int Multi(int num1, int num2)
	{
		int result = num1 * num2;
		return result;
	}
	float Divide(float num1, float num2)
	{
		float result = num1/ num2;
		return result;
	}
}
